class Variables{
    public static void main(String arg[]){
        byte b=5; //Declaration and Initialization
        System.out.println(b);
        b=10;
        System.out.println(b);
        short s=500;
        int j=900;
        float f=35.3f;
        char c='B';
        System.out.println(s);
        System.out.println(j);
        System.out.println(f);
        System.out.println(c);
    }
}